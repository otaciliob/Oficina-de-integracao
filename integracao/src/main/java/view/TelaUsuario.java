    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // Metodo para remover o usu치rio
        int confirma = JOptionPane.showConfirmDialog(null, "Quer mesmo excluir  o usu치rio?", "Aviso", JOptionPane.YES_NO_OPTION);
        Usuario usu = carregaUsuarios();
        if (confirma == JOptionPane.YES_OPTION) {
            if (dao.delete(usu)) {
                JOptionPane.showMessageDialog(null, "Usu치rio exclu칤do com sucesso");
                zerar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // Metodo para alterar o usu치rio
        Usuario usu = carregaUsuarios();
        if ((txtUsuUsuario.getText().isEmpty() || txtUsuSenha.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo obrigat칩rio");
        } else {
            if (dao.update(usu)) {
                JOptionPane.showMessageDialog(null, "Dados alterados com sucesso");
                zerar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // Metodo para cadastro do usu치rio
        Usuario usu = carregaUsuarios();
        if ((txtUsuUsuario.getText().isEmpty() || txtUsuSenha.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo obrigat칩rio");
        } else {
            if (dao.insert(usu)) {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
                zerar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void txtUsuPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuPesquisarKeyReleased
        // Evento enquanto est치 digitando
        pesquisar();
    }//GEN-LAST:event_txtUsuPesquisarKeyReleased

    public Usuario carregaUsuarios() {
        Usuario usu = null;
        try {
            String usuario = txtUsuUsuario.getText();
            String senha = txtUsuSenha.getText();
            usu = new Usuario(usuario, senha);
        } catch (NumberFormatException erro) {
            System.out.println(erro);
            JOptionPane.showMessageDialog(null, "Erro");
        }

        return usu;
    }

    public void zerar() {
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setNumRows(0);

        txtUsuUsuario.setText(null);
        txtUsuSenha.setText(null);
        for (Usuario usu : dao.select()) {
            modelo.addRow(new Object[]{
                usu.getUser(),
                usu.getPassword()
            });
        }
        pesquisar();
    }

    private void pesquisar() {
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setNumRows(0);

        String usuario = txtUsuPesquisar.getText();
        for (Usuario usu : dao.selectFrom(usuario)) {
            modelo.addRow(new Object[]{
                usu.getUser(),
                usu.getPassword()
            });
        }
    }

