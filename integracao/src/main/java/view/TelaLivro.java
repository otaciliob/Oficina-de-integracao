    public Livros carregaLivros() {
        Livros liv = null;
        try {
            int idli = Integer.valueOf(txtLivId.getText());
            String tituloli = txtLivTitulo.getText();
            String autorli = txtLivAutor.getText();
            int anoli = 0;
            int unidadesli = 0;
            if (txtLivAno.getText().length() > 0) {
                anoli = Integer.valueOf(txtLivAno.getText());
            }
            if (txtLivUnidades.getText().length() > 0) {
                unidadesli = Integer.valueOf(txtLivUnidades.getText());
            }
            liv = new Livros(idli, tituloli, autorli, anoli, unidadesli);
        } catch (NumberFormatException erro) {
            System.out.println(erro);
            JOptionPane.showMessageDialog(null, "Erro");
        }

        return liv;
    }
    
    public void zerar() {
        DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();
        modelo.setNumRows(0);
        
        txtLivId.setText("0");
        txtLivTitulo.setText(null);
        txtLivAutor.setText(null);
        txtLivAno.setText(null);
        txtLivUnidades.setText(null);
        txtLivPesquisar.setText(null);
        for (Livros liv : dao.select()) {
            modelo.addRow(new Object[]{
                liv.getId(),
                liv.getNome(),
                liv.getAutor(),
                liv.getAno(),
                liv.getUnidades()
            });
        }
        pesquisar();
    }

    private void pesquisar() {
        DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();
        modelo.setNumRows(0);
        
        String titulo = txtLivPesquisar.getText();
            for (Livros liv : dao.selectFrom(titulo)) {
                modelo.addRow(new Object[]{
                   liv.getId(),
                    liv.getNome(),
                    liv.getAutor(),
                    liv.getAno(),
                    liv.getUnidades()
                });
            }
    }
    
