    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // Metodo para remover o livro
        int confirma = JOptionPane.showConfirmDialog(null, "Quer mesmo excluir  o livro?", "Aviso", JOptionPane.YES_NO_OPTION);
        Livros liv = carregaLivros();
        if (confirma == JOptionPane.YES_OPTION) {
            if (dao.delete(liv)) {
                JOptionPane.showMessageDialog(null, "Livro excluído com sucesso");
                zerar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // Metodo para alterar o livro
        Livros liv = carregaLivros();
        if ((txtLivId.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo obrigatório");
        } else {
            if (dao.update(liv, Integer.valueOf(txtLivId.getText()))) {
                JOptionPane.showMessageDialog(null, "Dados alterados com sucesso");
                zerar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // Metodo para cadastro do livro
        Livros liv = carregaLivros();
        if ((txtLivTitulo.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo obrigatório");
        } else {
            if (dao.insert(liv)) {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
                zerar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro");
            }
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

